# datatypes
aString = "bhavan"
print(type(aString))

anInt = 23
print(type(anInt))

aFloat = 23.09
print(type(aFloat))

aComplex = 2 + 4j
print(type(aComplex))

aList = [1, 2, 3]
print(type(aList))

aTuple = (1, 2, 3)
print(type(aTuple))

aRange = range(10)
print(type(aRange))

aDict = {"name" : "Bhavan", "age" : 21}
print(type(aDict))

aSet = {1, 2, 3, 3}
print(aSet)
print(type(aSet))

aBool = True
print(type(aBool))